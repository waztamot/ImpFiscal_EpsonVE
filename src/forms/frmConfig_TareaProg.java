    /**
     * Cancelar el proceso de configuracion TareaProg
     * @param evt 
     */
    private void jcmdCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcmdCancelarActionPerformed
        // Variables
        String value;
        
        //  Proceso
        jlblNotificacionMouseExited(null);
        
        //  Proceso - Espera Ejecucion de la Tarea Prog
        value = this.configTareaProg.getProperty("tp_tiempo_espera_seg").trim();
        this.jspnRetardoEjecTareaP.setValue(Integer.valueOf(value.trim()));
        value = "";
        
        //  Proceso - Intervalo de Ejecucion de la Tarea Prog
        value = this.configTareaProg.getProperty("tp_intervalo_min").trim();
        this.jspnIntervaloEjecTareaP.setValue(Integer.valueOf(value.trim()));
        value = "";
        
        //  Proceso - Activar la Tarea Prog
        value = this.configTareaProg.getProperty("tp_ejecutar");
        if (value.trim().equalsIgnoreCase("S")){
            this.jckbActivarTareaP.setSelected(true);
            this.jspnIntervaloEjecTareaP.setEnabled(true);
            this.jspnRetardoEjecTareaP.setEnabled(true);
        } else {
            this.jckbActivarTareaP.setSelected(false);
            this.jspnIntervaloEjecTareaP.setEnabled(false);
            this.jspnRetardoEjecTareaP.setEnabled(false);
        }
    }//GEN-LAST:event_jcmdCancelarActionPerformed

    /**
     * Guardar datos de configuracion TareaProg
     * @param evt 
     */
    private void jcmdGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcmdGuardarActionPerformed
        //  Variables
        String value;
        
        //  Proceso
        jlblNotificacionMouseExited(null);
        
        //  Proceso - Guardar
        try {
            //  Proceso - Activar la Tarea Prog
            if (this.jckbActivarTareaP.isSelected())
                value = "S";
            else
                value = "N";
            this.configTareaProg.setProperty("tp_ejecutar", value.trim());
            value = "";
            
            //  Proceso - Espera Ejecucion de la Tarea Prog
            value = this.jspnRetardoEjecTareaP.getValue().toString().trim();
            this.configTareaProg.setProperty("tp_tiempo_espera_seg", value.trim());
            value = "";
            
            //  Proceso - Intervalo de Ejecucion de la Tarea Prog
            value = this.jspnIntervaloEjecTareaP.getValue().toString().trim();
            this.configTareaProg.setProperty("tp_intervalo_min", value.trim());
            
            //  Mensaje del sistema
            this.jlblNotificacion.setIcon(iconos.getSuccessImage());
            this.jlblNotificacion.setText("Guardado Exitosamente!");
            this.setOk(true);
        } catch (Exception ex) {
            this.jlblNotificacion.setIcon(iconos.getErrorImage());
            this.jlblNotificacion.setText("Error no se pudo guardar la informacion!");
            Logger.getLogger(frmConfig_TareaProg.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jcmdGuardarActionPerformed

