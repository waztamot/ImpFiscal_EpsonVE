    /**
     * Restaurar valores por defecto de la TareaProg
     * @param evt 
     */
    private void jlblValoresDefectMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlblValoresDefectMouseClicked
        
        //  Proceso
        jlblNotificacionMouseExited(null);
        try {
            //  Proceso - Espera Ejecucion de la Tarea Prog
            this.configTareaProg.setProperty("tp_tiempo_espera_seg", this.
                    configTareaProg.getProperty("tp_tiempo_espera_seg_def")
                    .trim());         
            //  Proceso - Intervalo de Ejecucion de la Tarea Prog
            this.configTareaProg.setProperty("tp_intervalo_min", this.
                    configTareaProg.getProperty("tp_intervalo_min_def").trim());
            //  Proceso - Activar la Tarea Prog
            this.configTareaProg.setProperty("tp_ejecutar", "N");
            //  Incializar Pantalla
            jcmdCancelarActionPerformed(null);
            //  Mensaje por pantalla
            this.jlblNotificacion.setIcon(iconos.getSuccessImage());
            this.jlblNotificacion.setText("Restaurado Exitosamente!");
            this.setOk(true);
        } catch (Exception ex) {
            this.jlblNotificacion.setIcon(iconos.getErrorImage());
            this.jlblNotificacion.setText("Error no se pudo restaurar la informacion!");
            Logger.getLogger(frmConfig_TareaProg.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jlblValoresDefectMouseClicked

