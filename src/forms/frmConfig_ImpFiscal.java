    /**
     * Cancelar el proceso de configuracion ImpFiscal
     * @param evt 
     */
    private void jcmdCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcmdCancelarActionPerformed
        // Variables
        String value = "";
        
        //  Proceso
        jlblNotificacionMouseExited(null);
        
        //  Proceso - Puerto COM
        value = this.configFiscal.getProperty("puerto_imp").toString().trim();
        this.jcmbbPuertoCOM.setSelectedItem(value);
        value = "";
        
        // Proceso - Velocidad Puerto COM
        value = this.configFiscal.getProperty("velocidad_imp").toString().trim();
        this.jspnVelocidadCOM.setValue(Integer.valueOf(value));
        
    }//GEN-LAST:event_jcmdCancelarActionPerformed

    /**
     * Guardar datos de configuracion ImpFiscal
     * @param evt 
     */
    private void jcmdGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcmdGuardarActionPerformed
        //  Variables
        String value = "";
        
        //  Proceso
        jlblNotificacionMouseExited(null);
        
        //  Proceso - Guardar
        try {
            //  Proceso - Puerto COM
            value = this.jcmbbPuertoCOM.getSelectedItem().toString().trim();
            this.configFiscal.setProperty("puerto_imp", value);
            value = "";
            
            // Proceso - Velocidad Puerto COM
            value = this.jspnVelocidadCOM.getValue().toString().trim();
            this.configFiscal.setProperty("velocidad_imp", value);
            
            //  Mensaje del sistema
            this.jlblNotificacion.setIcon(iconos.getSuccessImage());
            this.jlblNotificacion.setText("Guardado Exitosamente!");
            this.setOk(true);
        } catch (Exception ex) {
            this.jlblNotificacion.setIcon(iconos.getErrorImage());
            this.jlblNotificacion.setText("Error no se pudo guardar la informacion!");
            Logger.getLogger(frmConfig_ImpFiscal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jcmdGuardarActionPerformed

